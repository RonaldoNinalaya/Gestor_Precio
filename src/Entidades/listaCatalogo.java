package Entidades;

/**
 *
 * @author Ronaldo
 */
public class listaCatalogo {
    private String camisa;
    private String pantalones;
    private String abrigos;
    private String Vestidos;
    private String zapatillas;
    private String precio;
    /**
     * @return the camisa
     */
    
    
    public String getCamisa() {
        return camisa;
    }

    /**
     * @param camisa the camisa to set
     */
    public void setCamisa(String camisa) {
        this.camisa = camisa;
    }

    /**
     * @return the pantalones
     */
    public String getPantalones() {
        return pantalones;
    }

    /**
     * @param pantalones the pantalones to set
     */
    public void setPantalones(String pantalones) {
        this.pantalones = pantalones;
    }

    /**
     * @return the abrigos
     */
    public String getAbrigos() {
        return abrigos;
    }

    /**
     * @param abrigos the abrigos to set
     */
    public void setAbrigos(String abrigos) {
        this.abrigos = abrigos;
    }

    /**
     * @return the Vestidos
     */
    public String getVestidos() {
        return Vestidos;
    }

    /**
     * @param Vestidos the Vestidos to set
     */
    public void setVestidos(String Vestidos) {
        this.Vestidos = Vestidos;
    }

    /**
     * @return the zapatillas
     */
    public String getZapatillas() {
        return zapatillas;
    }

    /**
     * @param zapatillas the zapatillas to set
     */
    public void setZapatillas(String zapatillas) {
        this.zapatillas = zapatillas;
    }

    /**
     * @return the precio
     */
    public String getPrecio() {
        return precio;
    }

    /**
     * @param precio the precio to set
     */
    public void setPrecio(String precio) {
        this.precio = precio;
    }
    
}
